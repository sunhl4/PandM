/**
 * vite-plugin-scan.js
 *
 * Vite plugin that exposes `virtual:scanned-graph` — a virtual ES module
 * containing all auto-discovered nodes/edges from the materials/ directory.
 *
 * Usage in vite.config.js:
 *   import { scanGraphPlugin } from './vite-plugin-scan.js'
 *   plugins: [react(), scanGraphPlugin()]
 *
 * Import in your app:
 *   import { SCANNED_GRAPH } from 'virtual:scanned-graph'
 */

import path   from 'path'
import { scan, ROOT } from './scripts/scan-materials.js'

const VIRTUAL_ID          = 'virtual:scanned-graph'
const RESOLVED_VIRTUAL_ID = '\0virtual:scanned-graph'

const MATERIALS_DIR = path.join(ROOT, 'materials')

let cachedCode   = null
let cachedResult = null

function buildCode() {
  const result = scan()
  const json   = JSON.stringify(result, null, 2)
  cachedResult = result
  return (
    `// AUTO-GENERATED by vite-plugin-scan — do not edit manually\n` +
    `export const SCANNED_GRAPH = ${json}\n`
  )
}

export function scanGraphPlugin() {
  return {
    name: 'vite-plugin-scan-graph',
    enforce: 'pre',

    // ── Module resolution ───────────────────────────────────────────────────
    resolveId(id) {
      if (id === VIRTUAL_ID) return RESOLVED_VIRTUAL_ID
    },

    load(id) {
      if (id !== RESOLVED_VIRTUAL_ID) return
      if (!cachedCode) {
        cachedCode = buildCode()
        console.log(`[scan-graph] ✓  ${cachedResult.nodes.length} auto-nodes  ${cachedResult.edges.length} auto-edges`)
      }
      return cachedCode
    },

    // ── Dev-server file watcher ─────────────────────────────────────────────
    configureServer(server) {
      // Tell Vite to also watch the materials directory
      server.watcher.add(MATERIALS_DIR)

      let debounceTimer = null

      server.watcher.on('all', (event, filePath) => {
        // Only react to changes inside materials/
        const normalised = filePath.replace(/\\/g, '/')
        if (!normalised.includes('/materials/')) return

        // Ignore file types we don't care about
        const name = path.basename(filePath)
        const ext  = path.extname(name).toLowerCase()
        const interesting = ['.md', '.ipynb', '.json'].includes(ext)
        if (!interesting) return

        clearTimeout(debounceTimer)
        debounceTimer = setTimeout(() => {
          // Invalidate the cache
          cachedCode   = null
          cachedResult = null

          // Invalidate Vite's module graph entry
          const mod = server.moduleGraph.getModuleById(RESOLVED_VIRTUAL_ID)
          if (mod) server.moduleGraph.invalidateModule(mod)

          // Trigger a full-reload (state is in localStorage — no data loss)
          server.ws.send({ type: 'full-reload' })
          console.log(`[scan-graph] ↺  ${event} ${path.relative(ROOT, filePath)} → graph refreshed`)
        }, 300)
      })
    },
  }
}
